#!/usr/bin/env python3
"""DM database migration helper functions."""
from __future__ import annotations

import os
import stat
from pathlib import Path
from typing import Any, Dict, List, Optional
from xml.sax.saxutils import escape


def find_dts_cli(extra_candidates: Optional[List[str]] = None) -> Optional[str]:
    candidates = extra_candidates or []
    dm_home = os.environ.get("DM_HOME", "")
    if dm_home:
        candidates.insert(0, str(Path(dm_home) / "tool" / "dts_cmd_run.sh"))
    candidates.extend([
        "/opt/dmdbms/tool/dts_cmd_run.sh",
        "/dm8/tool/dts_cmd_run.sh",
        "/opt/dm8/tool/dts_cmd_run.sh",
    ])
    for c in candidates:
        if not c:
            continue
        p = Path(os.path.expandvars(c))
        if p.exists() and os.access(p, os.X_OK):
            return str(p)
    # Lightweight bounded search.
    for root in [Path("/opt"), Path("/dm8")]:
        if not root.exists():
            continue
        for p in root.glob("**/tool/dts_cmd_run.sh"):
            if p.exists() and os.access(p, os.X_OK):
                return str(p)
    return None


def source_compatibility(source_type: str) -> str:
    mapping = {
        "mysql": "mysql-compatible",
        "oracle": "oracle-compatible",
        "sqlserver": "sqlserver-compatible",
        "mssql": "sqlserver-compatible",
        "db2": "db2-compatible",
        "dm": "dm-native",
    }
    return mapping.get(source_type.lower(), "auto")


def generate_dts_xml(job: Dict[str, Any]) -> str:
    """Generate a conservative DTS XML skeleton.

    Real DTS XML may need fields adjusted to the exact DM DTS version. This skeleton is intended
    as a Skill-generated draft that must be summarized and confirmed before execution.
    """
    source = job.get("source", {})
    target = job.get("target", {})
    objects = job.get("objects", {"mode": "schema", "include": [source.get("schema") or source.get("database") or ""]})
    def e(x: Any) -> str:
        return escape(str(x or ""))
    include_items = "\n".join(f"      <object>{e(x)}</object>" for x in objects.get("include", []))
    return f'''<?xml version="1.0" encoding="UTF-8"?>
<!--
  DM DTS CLI migration job draft generated by ai-system-migration Skill.
  Review and adapt to the exact DM DTS CLI version before running.
-->
<dts-job>
  <job-name>{e(job.get('name', 'ai-system-migration-dts-job'))}</job-name>
  <migration-mode>{e(job.get('migration_mode', 'full'))}</migration-mode>
  <source>
    <type>{e(source.get('type'))}</type>
    <host>{e(source.get('host'))}</host>
    <port>{e(source.get('port'))}</port>
    <database>{e(source.get('database'))}</database>
    <schema>{e(source.get('schema'))}</schema>
    <username>{e(source.get('username'))}</username>
    <password>${{SOURCE_DB_PASSWORD}}</password>
  </source>
  <target>
    <type>dm</type>
    <host>{e(target.get('host', '127.0.0.1'))}</host>
    <port>{e(target.get('port', 5236))}</port>
    <database>{e(target.get('database'))}</database>
    <schema>{e(target.get('schema'))}</schema>
    <username>{e(target.get('username'))}</username>
    <password>${{TARGET_DB_PASSWORD}}</password>
  </target>
  <objects mode="{e(objects.get('mode', 'schema'))}">
{include_items}
  </objects>
  <options>
    <migrate-tables>true</migrate-tables>
    <migrate-data>true</migrate-data>
    <migrate-indexes>true</migrate-indexes>
    <migrate-constraints>true</migrate-constraints>
    <migrate-views>true</migrate-views>
    <migrate-procedures>false</migrate-procedures>
    <migrate-triggers>false</migrate-triggers>
  </options>
</dts-job>
'''


def dts_summary(job: Dict[str, Any], dts_cli: Optional[str]) -> Dict[str, Any]:
    source = job.get("source", {})
    target = job.get("target", {})
    return {
        "source_type": source.get("type"),
        "source_endpoint": f"{source.get('host')}:{source.get('port')}",
        "source_database": source.get("database"),
        "source_schema": source.get("schema"),
        "target_type": "dm",
        "target_endpoint": f"{target.get('host', '127.0.0.1')}:{target.get('port', 5236)}",
        "target_database": target.get("database"),
        "target_schema": target.get("schema"),
        "compatibility": source_compatibility(str(source.get("type") or "")),
        "dts_cli_found": bool(dts_cli),
        "dts_cli": dts_cli or "not-found",
        "requires_confirmation_before_run": True,
    }
